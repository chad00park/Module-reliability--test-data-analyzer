        vlh = tk.StringVar(value="" if cl.get("high") is None else str(cl["high"]))
        ttk.Entry(dlg, textvariable=vlh, width=10).grid(row=2, column=1, padx=5, pady=3)
        ttk.Label(dlg, text="High:").grid(row=2, column=2, sticky="e", padx=5)
        vdh = tk.StringVar(value="" if cd.get("high") is None else str(cd["high"]))
        ttk.Entry(dlg, textvariable=vdh, width=10).grid(row=2, column=3, padx=5, pady=3)
        ttk.Label(dlg, text="Low:").grid(row=3, column=0, sticky="e", padx=5)
        vll = tk.StringVar(value="" if cl.get("low") is None else str(cl["low"]))
        ttk.Entry(dlg, textvariable=vll, width=10).grid(row=3, column=1, padx=5, pady=3)
        ttk.Label(dlg, text="Low:").grid(row=3, column=2, sticky="e", padx=5)
        vdl = tk.StringVar(value="" if cd.get("low") is None else str(cd["low"]))
        ttk.Entry(dlg, textvariable=vdl, width=10).grid(row=3, column=3, padx=5, pady=3)
        ttk.Label(dlg, text="※ 빈칸이면 해당 limit 없음. Box plot은 Read-out limit 적용\n"
                            "※ limit 밖 data는 빈 동그라미 마커로 표시됩니다",
                  foreground="gray").grid(row=4, column=0, columnspan=4, pady=3)

        def _parse(sv):
            t = sv.get().strip().replace(",", "").replace(" ", "")
            if not t:
                return None
            return float(t)

        def apply():
            try:
                lh, ll, dh, dl = _parse(vlh), _parse(vll), _parse(vdh), _parse(vdl)
            except ValueError:
                messagebox.showwarning("알림", "숫자를 입력하세요.", parent=dlg)
                return
            for hi, lo, nm in ((lh, ll, "Read-out"), (dh, dl, "Delta %")):
                if hi is not None and lo is not None and hi <= lo:
                    messagebox.showwarning(
                        "알림", f"{nm}의 High limit는 Low limit보다 커야 합니다.",
                        parent=dlg)
                    return
            self.model.set_limits(lot, col, lh, ll, dh, dl)
            dlg.destroy()
            self._redraw()

        btns = ttk.Frame(dlg)
        btns.grid(row=5, column=0, columnspan=4, pady=10)
        ttk.Button(btns, text="적용", command=apply).pack(side="left", padx=5)
        ttk.Button(btns, text="취소", command=dlg.destroy).pack(side="left", padx=5)
        center_window(dlg)

