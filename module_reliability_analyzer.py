# 3. 데이터 모델 — Lot 그룹 × (phase, col) 키
# ============================================================================
class DataModel:
    """
    g[lot] = {
        'readouts': 정렬된 Read-out 목록,
        'phases'  : phase 목록 (파일 등장 순서),
        'rep_cols': phase 내 반복 item 컬럼명 목록 (등장 순서, phase 간 합집합),
        'np_cols' : phase 밖(공통) 컬럼명 목록,
        'samples' : 정렬된 시료 번호,
        'data'    : data[readout][sample][(phase, colname)] = float|None
    }
    편집 키: (lot, readout, colname, phase, sample)
    """

    def __init__(self):
        self.reliability = None
        self.groups = []
        self.g = {}
        self.deleted = set()
        self.color_over = {}
        self.ylim = {}          # (lot, colname) -> (min,max)
        self.limits = {}        # (lot, colname) -> {'high': v|None, 'low': v|None}
        self.custom_lines = {}  # (lot, colname, kind) -> [ {axis,value,style,color,name} ]
        self.texts = {}         # (lot, colname, kind) -> [ {text,x,y,size,color} ]
        self._undo = []
        self._redo = []

    # ---- 로드 ------------------------------------------------------------
    def load(self, files):
        errors = []
        raw = {}
        rels = {}
        for p in files:
            fname = os.path.basename(p)
            try:
                rel, lot, rl, rv, is_rt = parse_filename(p)
                phases, cols, d = parse_data_file(p)
            except ValueError as e:
                errors.append(str(e))
                continue
            rels.setdefault(rel, []).append(fname)
            raw.setdefault(lot, []).append((rl, rv, is_rt, phases, cols, d, fname))

        if not raw:
            return errors
        if len(rels) > 1:
            detail = "\n".join(f"  {r}: {', '.join(fs)}" for r, fs in rels.items())
            errors.append("한 번에 하나의 신뢰성만 분석할 수 있습니다.\n"
                          f"여러 신뢰성이 섞여 있습니다:\n{detail}")
            return errors
        self.reliability = next(iter(rels))

        self.groups = []
        self.g = {}
        for lot in sorted(raw):
            gkey = lot
            entries = raw[lot]
            seen_ro = {}
            dup = []
            for rl, rv, is_rt, phases, cols, d, fname in entries:
                if rl in seen_ro:
                    dup.append(f"{gkey}의 {rl}: '{seen_ro[rl]}' 와 '{fname}'")
                else:
                    seen_ro[rl] = fname
            if dup:
                errors.append("동일 Lot에 같은 Read-out 파일이 중복되었습니다:\n"
                              + "\n".join(dup))
                continue
            if len(entries) > MAX_READOUTS:
                errors.append(f"{gkey}: Read-out 파일이 {MAX_READOUTS}개를 "
                              f"초과합니다 ({len(entries)}개).")
                continue

            # 정렬: read-out 시간 → retest는 동일 시간 바로 다음
            entries.sort(key=lambda x: (x[1], x[2]))
            readouts = [e[0] for e in entries]
            phase_order, rep_cols, np_cols = [], [], []
            samp_union = set()
            data = {}
            for rl, rv, is_rt, phases, cols, d, fname in entries:
                for ph in phases:
                    if ph not in phase_order:
                        phase_order.append(ph)
                for ph, c in cols:
                    if ph == NO_PHASE:
                        if c not in np_cols:
                            np_cols.append(c)
                    else:
                        if c not in rep_cols:
                            rep_cols.append(c)
                samp_union.update(d.keys())
                data[rl] = d

            n_params = len(rep_cols) + len(np_cols)
            if n_params > MAX_PARAMS:
                errors.append(f"{gkey}: Parameter가 {MAX_PARAMS}개를 초과합니다 "
                              f"({n_params}개).")
                continue
            if len(samp_union) > MAX_SAMPLES:
                errors.append(f"{gkey}: Sample이 {MAX_SAMPLES}개를 초과합니다 "
                              f"({len(samp_union)}개).")
                continue

            self.groups.append(gkey)
            self.g[gkey] = dict(readouts=readouts, phases=phase_order,
                                rep_cols=rep_cols, np_cols=np_cols,
                                samples=sorted(samp_union), data=data)

        self.deleted.clear()
        self.color_over.clear()
        self.ylim.clear()
        self.limits.clear()
        self.custom_lines.clear()
        self.texts.clear()
        self._undo.clear()
        self._redo.clear()
        return errors

    # ---- 접근자 ----------------------------------------------------------
    def group_rel(self, lot):
        return self.reliability

    def group_lot(self, lot):
        return lot

    def readouts(self, lot):
        return self.g[lot]["readouts"]

    def phases(self, lot):
        return self.g[lot]["phases"]

    def rep_cols(self, lot):
        return self.g[lot]["rep_cols"]

    def np_cols(self, lot):
        return self.g[lot]["np_cols"]

    def samples(self, lot):
        return self.g[lot]["samples"]

    def col_phases(self, lot, col):
        """해당 컬럼이 그려질 phase 목록. 공통 컬럼이면 [NO_PHASE]."""
        if col in self.g[lot]["np_cols"]:
            return [NO_PHASE]
        return self.g[lot]["phases"]

    # ---- 값 조회 -----------------------------------------------------------
    def value(self, lot, readout, col, phase, sample):
        if (lot, readout, col, phase, sample) in self.deleted:
            return None
        return self.g[lot]["data"].get(readout, {}).get(sample, {}).get((phase, col))

    def seg_series(self, lot, readout, col):
        """phase 구간을 이어 붙인 시리즈.
        반환: (positions, values, seg_info)
          seg_info: [(phase, start_pos, end_pos)] — 구간 경계/이름 표기에 사용"""
        samples = self.samples(lot)
        N = len(samples)
        phs = self.col_phases(lot, col)
        xs, ys = [], []
        segs = []
        for pi, ph in enumerate(phs):
            base = pi * N
            segs.append((ph, base + 1, base + N))
            for si, s in enumerate(samples):
                xs.append(base + si + 1)
                v = self.value(lot, readout, col, ph, s)
                ys.append(math.nan if v is None else v)
        return xs, ys, segs

    def seg_delta(self, lot, readout, col):
        """초기 Read-out 대비 변화율(%) — 같은 phase 같은 시료끼리 비교."""
        base_ro = self.readouts(lot)[0]
        samples = self.samples(lot)
        N = len(samples)
        phs = self.col_phases(lot, col)
        xs, ys = [], []
        segs = []
        for pi, ph in enumerate(phs):
            base = pi * N
            segs.append((ph, base + 1, base + N))
            for si, s in enumerate(samples):
                xs.append(base + si + 1)
                v0 = self.value(lot, base_ro, col, ph, s)
                v = self.value(lot, readout, col, ph, s)
                if v0 is None or v is None or v0 == 0:
                    ys.append(math.nan)
                else:
                    ys.append((v - v0) / v0 * 100.0)
        return xs, ys, segs

    def pos_to_phase_sample(self, lot, col, pos_index):
        """seg_series의 인덱스 → (phase, sample)."""
        samples = self.samples(lot)
        N = len(samples)
        phs = self.col_phases(lot, col)
        pi = pos_index // N
        si = pos_index % N
        if pi >= len(phs):
            return None, None
        return phs[pi], samples[si]

    def box_values(self, lot, readout, col, phase=None):
        """phase 지정 시 해당 phase만, None이면 모든 phase stack."""
        phs = [phase] if phase is not None else self.col_phases(lot, col)
        out = []
        for ph in phs:
            for s in self.samples(lot):
                v = self.value(lot, readout, col, ph, s)
                if v is not None:
                    out.append(v)
        return out

    # ---- diff 기반 Undo/Redo ------------------------------------------------
    def _apply(self, action, forward=True):
        kind = action[0]
        if kind == "del":
            _, keys = action
            if forward:
                self.deleted.update(keys)
            else:
                self.deleted.difference_update(keys)
        elif kind == "color":
            _, key, old, new = action
            c = new if forward else old
            if c is None:
                self.color_over.pop(key, None)
            else:
                self.color_over[key] = c
        elif kind == "ylim":
            _, key, old, new = action
            v = new if forward else old
            if v is None:
                self.ylim.pop(key, None)
            else:
                self.ylim[key] = v
        elif kind == "limits":
            _, key, old, new = action
            v = new if forward else old
            if not v:
                self.limits.pop(key, None)
            else:
                self.limits[key] = v
        elif kind == "cline":
            _, key, old, new = action
            v = new if forward else old
            if not v:
                self.custom_lines.pop(key, None)
            else:
                self.custom_lines[key] = v
        elif kind == "text":
            _, key, old, new = action
            v = new if forward else old
            if not v:
                self.texts.pop(key, None)
            else:
                self.texts[key] = v

    def do(self, action):
        self._apply(action, True)
        self._undo.append(action)
        self._redo.clear()

    def undo(self):
        if not self._undo:
            return False
        a = self._undo.pop()
        self._apply(a, False)
        self._redo.append(a)
        return True

    def redo(self):
        if not self._redo:
            return False
        a = self._redo.pop()
        self._apply(a, True)
        self._undo.append(a)
        return True

    # ---- 편집 액션 -----------------------------------------------------------
    def delete_point(self, lot, readout, col, phase, sample):
        self.do(("del", frozenset({(lot, readout, col, phase, sample)})))

    def delete_sample_all_readouts(self, lot, col, phase, sample):
        keys = frozenset((lot, r, col, phase, sample) for r in self.readouts(lot))
        self.do(("del", keys))

    def set_color(self, lot, readout, col, phase, sample, color):
        key = (lot, readout, col, phase, sample)
        self.do(("color", key, self.color_over.get(key), color))

    def set_limits(self, lot, col, line_hi, line_lo, delta_hi, delta_lo):
        """Read-out용/Delta %용 limit을 각각 저장. Box는 Read-out limit을 따름."""
        key = (lot, col)
        old = {k: dict(v) for k, v in self.limits.get(key, {}).items()}
        new = {}
        line_d = {}
        if line_hi is not None:
            line_d["high"] = line_hi
        if line_lo is not None:
            line_d["low"] = line_lo
        if line_d:
            new["line"] = line_d
        delta_d = {}
        if delta_hi is not None:
            delta_d["high"] = delta_hi
        if delta_lo is not None:
            delta_d["low"] = delta_lo
        if delta_d:
            new["delta"] = delta_d
        self.do(("limits", key, old, new))

    def set_custom_lines(self, lot, col, kind, lines):
        key = (lot, col, kind)
        old = list(self.custom_lines.get(key, []))
        self.do(("cline", key, old, list(lines)))

    def set_texts(self, lot, col, kind, texts):
        key = (lot, col, kind)
        old = list(self.texts.get(key, []))
        self.do(("text", key, old, list(texts)))

    def set_ylim(self, lot, col, ymin, ymax):
        key = (lot, col)
        new = None if (ymin is None and ymax is None) else (ymin, ymax)
        self.do(("ylim", key, self.ylim.get(key), new))

    # ---- 통계 -----------------------------------------------------------------
    def stats(self, lot, readout, col, phase=None):
        vals = self.box_values(lot, readout, col, phase)
        n = len(vals)
        if n == 0:
            return dict(SS=0, Min=math.nan, Max=math.nan, AVG=math.nan, STD=math.nan)
        avg = sum(vals) / n
        std = (sum((v - avg) ** 2 for v in vals) / (n - 1)) ** 0.5 if n > 1 else 0.0
        return dict(SS=n, Min=min(vals), Max=max(vals), AVG=avg, STD=std)


