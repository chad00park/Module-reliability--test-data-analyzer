def _logo_bytes():
    """로고 PNG 바이트: 스크립트 옆에 logo.png가 있으면 그것을(고해상도),
    없으면 소스에 내장된 로고를 사용."""
    try:
        if os.path.exists(LOGO_PATH):
            with open(LOGO_PATH, "rb") as f:
                return f.read()
    except Exception:
        pass
    import base64
    return base64.b64decode(_LOGO_B64)


def load_logo_image(height):
    """로고를 지정 높이(px)로 선명하게 리사이즈한 Tk 이미지 반환 (없으면 None)."""
    try:
        from PIL import Image, ImageTk
        img = Image.open(io.BytesIO(_logo_bytes()))
        w = max(1, int(img.width * height / img.height))
        return ImageTk.PhotoImage(img.resize((w, height), Image.LANCZOS))
    except Exception:
        return None


def add_pdf_logo(fig, h_in=0.34):
    """PDF 페이지 좌측 상단에 로고 (높이 0.34in, 종횡비 유지, 선명)."""
    try:
        logo = mpimg.imread(io.BytesIO(_logo_bytes()), format="png")
    except Exception:
        return
    lh, lw = logo.shape[0], logo.shape[1]
    w_in = h_in * lw / lh
    fw, fh = fig.get_size_inches()
    ax = fig.add_axes([0.007, 1 - (h_in / fh) - 0.006, w_in / fw, h_in / fh])
    ax.imshow(logo, interpolation="lanczos")
    ax.axis("off")


