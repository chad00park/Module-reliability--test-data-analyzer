LIMIT_COLOR = "#d62728"


def _collect_pts(ax):
    pts_x, pts_y = [], []
    for ln in ax.get_lines():
        if ln.get_marker() == "o":
            for x, y in zip(ln.get_xdata(), ln.get_ydata()):
                try:
                    if not math.isnan(float(y)):
                        pts_x.append(float(x))
                        pts_y.append(float(y))
                except (TypeError, ValueError):
                    pass
    return pts_x, pts_y


def _layout_ctx(ax, pts_x, pts_y):
    """렌더러 실측 기반 배치 컨텍스트: (renderer, 데이터 픽셀 좌표, 축 픽셀 bbox)."""
    fig = ax.figure
    if getattr(fig, "canvas", None) is None or             not hasattr(fig.canvas, "get_renderer"):
        from matplotlib.backends.backend_agg import FigureCanvasAgg
        FigureCanvasAgg(fig)
    rend = fig.canvas.get_renderer()
    pix = [tuple(ax.transData.transform((x, y))) for x, y in zip(pts_x, pts_y)]
    return rend, pix, ax.get_window_extent(rend)


def _measure_text(ax, rend, name, fs=7):
    t = ax.text(0, 0, name, fontsize=fs)
    bb = t.get_window_extent(rend)
    t.remove()
    return bb.width, bb.height


def _box_free(box, pix, occupied, pad=2.0):
    bx0, bx1, by0, by1 = box
    for px, py in pix:
        if bx0 - pad <= px <= bx1 + pad and by0 - pad <= py <= by1 + pad:
            return False
    for o in occupied:
        if not (bx1 < o[0] or bx0 > o[1] or by1 < o[2] or by0 > o[3]):
            return False
    return True


def _place_hline_label(ax, v, name, color, ctx, occupied):
    """가로선 이름: 선 바로 윗부분을 좌우로 훑어 실측 픽셀로 빈 자리 탐색.
    실패 시 위/아래로 넓혀 탐색 후 가장 가까운 빈 자리에 배치 + 연결선."""
    rend, pix, axbb = ctx
    w, h = _measure_text(ax, rend, name)
    inv = ax.transData.inverted()
    _, vy = ax.transData.transform((ax.get_xlim()[0], v))
    # 1순위: 선 바로 위 (연결선 없음)
    ty0 = vy + 2
    if axbb.y0 <= ty0 and ty0 + h <= axbb.y1:
        for xf in [0.05, 0.15, 0.25, 0.35, 0.45, 0.55, 0.65, 0.75, 0.85]:
            cx = axbb.x0 + xf * axbb.width
            box = (cx, cx + w, ty0, ty0 + h)
            if box[1] > axbb.x1:
                continue
            if _box_free(box, pix, occupied):
                dx_, dy_ = inv.transform((cx, ty0))
                ax.text(dx_, dy_, name, ha="left", va="bottom",
                        fontsize=7, color=color, zorder=6)
                occupied.append(box)
                return
    # 2순위: 선에서 위/아래로 떨어진 빈 공간 + 연결선 (가까운 순 탐색)
    for k in [1.5, -1.5, 2.5, -2.5, 3.5, -3.5]:
        ty = vy + k * h
        if ty < axbb.y0 + 2 or ty + h > axbb.y1 - 2:
            continue
        for xf in [0.05, 0.2, 0.35, 0.5, 0.65, 0.8]:
            cx = axbb.x0 + xf * axbb.width
            box = (cx, cx + w, ty, ty + h)
            if box[1] > axbb.x1 or not _box_free(box, pix, occupied):
                continue
            tx_d, ty_d = inv.transform((cx, ty))
            ax_d, _ = inv.transform((cx + w / 2, vy))
            ax.annotate(name, xy=(ax_d, v), xytext=(tx_d, ty_d),
                        ha="left", va="bottom", fontsize=7, color=color,
                        zorder=6, arrowprops=dict(arrowstyle="-", color=color,
                                                  lw=0.7, alpha=0.9))
            occupied.append(box)
            return
    # 최후: 좌상단 고정 + 연결선
    cx, ty = axbb.x0 + 4, axbb.y1 - h - 4
    tx_d, ty_d = inv.transform((cx, ty))
    ax.annotate(name, xy=(inv.transform((cx + w / 2, vy))[0], v),
                xytext=(tx_d, ty_d), ha="left", va="bottom", fontsize=7,
                color=color, zorder=6,
                arrowprops=dict(arrowstyle="-", color=color, lw=0.7, alpha=0.9))
    occupied.append((cx, cx + w, ty, ty + h))


def _place_vline_label(ax, v, name, color, ctx, occupied):
    """세로선 이름: 선의 윗부분을 상하로 훑어 실측 픽셀로 빈 자리 탐색.
    실패 시 좌우 빈 공간 + 연결선."""
    rend, pix, axbb = ctx
    w, h = _measure_text(ax, rend, name)
    inv = ax.transData.inverted()
    vx, _ = ax.transData.transform((v, ax.get_ylim()[0]))
    for yf in [0.90, 0.80, 0.70, 0.60, 0.50, 0.40, 0.30]:
        cy = axbb.y0 + yf * axbb.height
        cx = vx + 3
        box = (cx, cx + w, cy - h / 2, cy + h / 2)
        if box[1] > axbb.x1 - 2:
            cx = vx - 3 - w
            box = (cx, cx + w, cy - h / 2, cy + h / 2)
        if box[0] < axbb.x0 + 2:
            continue
        if _box_free(box, pix, occupied):
            tx_d, ty_d = inv.transform((cx, cy))
            ax.text(tx_d, ty_d, name, ha="left", va="center",
                    fontsize=7, color=color, zorder=6)
            occupied.append(box)
            return
    # 폴백: 선에서 좌우로 떨어진 위치 + 연결선
    for koff in [1.0, -1.0, 2.0, -2.0]:
        cx = vx + koff * (w + 10)
        if cx < axbb.x0 + 2 or cx + w > axbb.x1 - 2:
            continue
        cy = axbb.y0 + 0.85 * axbb.height
        box = (cx, cx + w, cy - h / 2, cy + h / 2)
        if not _box_free(box, pix, occupied):
            continue
        tx_d, ty_d = inv.transform((cx, cy))
        ay_d = inv.transform((vx, cy))[1]
        ax.annotate(name, xy=(v, ay_d), xytext=(tx_d, ty_d),
                    ha="left", va="center", fontsize=7, color=color, zorder=6,
                    arrowprops=dict(arrowstyle="-", color=color, lw=0.7, alpha=0.9))
        occupied.append(box)
        return
    tx_d, ty_d = inv.transform((axbb.x0 + 4, axbb.y1 - h - 4))
    ax.annotate(name, xy=(v, ty_d), xytext=(tx_d, ty_d),
                ha="left", va="bottom", fontsize=7, color=color, zorder=6,
                arrowprops=dict(arrowstyle="-", color=color, lw=0.7, alpha=0.9))
    occupied.append((axbb.x0 + 4, axbb.x0 + 4 + w, axbb.y1 - h - 4, axbb.y1 - 4))


def draw_overlays(ax, model, group, col, kind):
    """High/Low limit(빨간 점선) + 임의의 선 + 임의 텍스트 렌더링.
    이름들은 서로/데이터와 겹치지 않게 배치."""
    pts_x, pts_y = _collect_pts(ax)
    ctx = _layout_ctx(ax, pts_x, pts_y)
    occupied = []
    # 1) High/Low limit — line/box는 Read-out limit, delta는 Delta % limit
    lim_kind = "delta" if kind == "delta" else "line"
    lim = model.limits.get((group, col), {}).get(lim_kind, {})
    if lim:
        for key, name in (("high", "High limit"), ("low", "Low limit")):
            if lim.get(key) is None:
                continue
            v = lim[key]
            ax.axhline(v, color=LIMIT_COLOR, ls=":", lw=1.2, zorder=4)
            _place_hline_label(ax, v, name, LIMIT_COLOR, ctx, occupied)
    # 2) 임의의 선
    for cl in model.custom_lines.get((group, col, kind), []):
        c = cl.get("color") or "#1f77b4"
        st = cl.get("style", ":")
        nm = cl.get("name", "")
        v = cl["value"]
        if cl["axis"] == "y":
            ax.axhline(v, color=c, ls=st, lw=1.2, zorder=4)
            if nm:
                _place_hline_label(ax, v, nm, c, ctx, occupied)
        else:
            ax.axvline(v, color=c, ls=st, lw=1.2, zorder=4)
            if nm:
                _place_vline_label(ax, v, nm, c, ctx, occupied)
    # 3) 임의 텍스트 (드래그 이동 지원 — GUI에서 picker 연결)
    artists = []
    for i, tx in enumerate(model.texts.get((group, col, kind), [])):
        txt = tx["text"]
        if any("\uac00" <= ch <= "\ud7a3" or "\u3131" <= ch <= "\u318e"
               for ch in txt):
            fam = ["Malgun Gothic", "AppleGothic", "NanumGothic", "DejaVu Sans"]
        else:
            fam = ["Arial", "DejaVu Sans"]
        t = ax.text(tx["x"], tx["y"], txt,
                    fontsize=tx.get("size", 10), color=tx.get("color", "black"),
                    fontfamily=fam, zorder=7, picker=True)
        t._user_text_index = i
        t._user_text_kind = kind
        artists.append(t)
    return artists


def _apply_default_ylim(ax):
    vmax = None
    for _ln in ax.get_lines():
        for _y in _ln.get_ydata():
            try:
                fy = float(_y)
            except (TypeError, ValueError):
                continue
            if not math.isnan(fy) and (vmax is None or fy > vmax):
                vmax = fy
    if vmax is not None and vmax > 0:
        ax.set_ylim(0, vmax * 1.10)
    else:
        ax.set_ylim(bottom=0)


def apply_overlays(fig):
    """tight_layout/subplots_adjust 이후 호출: 확정된 축 크기 기준으로
    limit/임의선/텍스트를 렌더링 (겹침 회피 정확도 보장)."""
    for ax in fig.axes:
        args = getattr(ax, "_overlay_args", None)
        if args is not None:
            draw_overlays(ax, *args)
            ax._overlay_args = None


def limit_out(model, group, col, v, kind="line"):
    """값 v가 해당 그래프(kind: line/delta)의 High/Low limit 밖인지.
    Box plot은 kind='line'(Read-out limit)을 따른다."""
    lim = model.limits.get((group, col), {}).get(kind, {})
    if not lim or v is None:
        return False
    hi = lim.get("high")
    lo = lim.get("low")
    return (hi is not None and v > hi) or (lo is not None and v < lo)


def _darken(color, factor=0.6):
    """색상을 어둡게 (limit 밖 마커 테두리용)."""
    try:
        import matplotlib.colors as mcolors
        r, g, b = mcolors.to_rgb(color)
        return (r * factor, g * factor, b * factor)
    except Exception:
        return color


def readout_color(model, lot, readout):
    return READOUT_COLORS[model.readouts(lot).index(readout) % len(READOUT_COLORS)]


def graph_title(model, lot, col, phase=None):
    t = f"{model.group_rel(lot)}: {model.group_lot(lot)}, {col_title(col)}"
    if phase is not None and phase != NO_PHASE:
        t += f" ({phase})"
    return t


def delta_title(model, lot, col):
    t = f"{model.group_rel(lot)}: {model.group_lot(lot)}, {col_title(col)}"
    return re.sub(r"\[[^\[\]]*\]", "[%]", t, count=1)


def _seg_axis(ax, model, lot, col, segs, screen=False):
    """구간형 X축: 시료 라벨(짝수) 반복 + phase 경계 점선 + phase 이름 표기.
    screen=True(프로그램 화면): tick 폰트 +2, phase 이름을 축에 근접 배치(고정 픽셀).
    screen=False(PDF): 기존 배치 유지."""
    samples = model.samples(lot)
    N = len(samples)
    multi = len(segs) > 1 or segs[0][0] != NO_PHASE
    ticks, labels = [], []
    for pi, (ph, start, end) in enumerate(segs):
        base = start - 1
        for si, s in enumerate(samples):
            ticks.append(base + si + 1)   # 홀수·짝수 모두 라벨 표기
            labels.append(str(s))
        if pi > 0:
            ax.axvline(start - 0.5, color="gray", lw=0.9, ls="--", alpha=0.8)
    ax.set_xticks(ticks)
    ax.set_xticklabels(labels)
    ax.set_xlim(0, segs[-1][2] + 1)
    ax.tick_params(labelsize=8 if screen else 6)
    if multi:
        for ph, start, end in segs:
            name = "" if ph == NO_PHASE else ph
            if screen:
                # 화면: tick 라벨 바로 아래 고정 픽셀 오프셋 (축 크기 무관하게 근접)
                ax.annotate(name, xy=((start + end) / 2, 0),
                            xycoords=("data", "axes fraction"),
                            xytext=(0, -18), textcoords="offset points",
                            ha="center", va="top", fontsize=8, fontweight="bold",
                            annotation_clip=False)
            else:
                trans = mtransforms.blended_transform_factory(ax.transData, ax.transAxes)
                ax.text((start + end) / 2, -0.30, name, transform=trans,
                        ha="center", va="top", fontsize=6, fontweight="bold")
        # phase 이름이 축 설명을 겸하므로 Sample No. 라벨은 생략 (겹침 방지)
    else:
        ax.set_xlabel("Sample No.", fontsize=8)


def draw_line(ax, model, lot, col, picker=False, screen=False):
    artists = {}
    segs = None
    base_ms = 3.5
    for r in model.readouts(lot):
        c = readout_color(model, lot, r)
        xs, ys, segs = model.seg_series(lot, r, col)
        # 선(마커 없음, picker용) + limit 안/밖 마커 분리
        kw = dict(lw=0.9, color=c, label=r)
        if picker:
            kw["picker"] = 5
        line, = ax.plot(xs, ys, **kw)
        artists[r] = line
        in_x, in_y, out_x, out_y = [], [], [], []
        for x, v in zip(xs, ys):
            if math.isnan(v):
                continue
            if limit_out(model, lot, col, v):
                out_x.append(x)
                out_y.append(v)
            else:
                in_x.append(x)
                in_y.append(v)
        ax.plot(in_x, in_y, marker="o", ms=base_ms, ls="none", color=c)
        if out_x:  # limit 밖: 빈 동그라미 (진한 테두리, 한 단계 굵고 크게)
            ax.plot(out_x, out_y, marker="o", ms=base_ms + 1, mfc="none",
                    mec=_darken(c), mew=1.6, ls="none", zorder=4)
        # 색 변경 마커(삼각형) — 우선 표시
        for idx, (x, v) in enumerate(zip(xs, ys)):
            ph, s = model.pos_to_phase_sample(lot, col, idx)
            oc = model.color_over.get((lot, r, col, ph, s))
            if oc and not math.isnan(v):
                ax.plot([x], [v], marker="^", ms=6, color=oc,
                        markeredgecolor="black", markeredgewidth=0.6,
                        ls="none", zorder=5)
    ax.set_title(graph_title(model, lot, col), fontsize=9, loc="left")
    mu = re.search(r"\(([^()]*)\)\s*(?:#\d+)?$", col)
    unit = f" ({mu.group(1)})" if mu else ""
    ax.set_ylabel(col.split("@")[0] + unit, fontsize=8)
    _seg_axis(ax, model, lot, col, segs, screen=screen)
    user_yl = model.ylim.get((lot, col))
    if user_yl and (user_yl[0] is not None or user_yl[1] is not None):
        # 기본값 먼저 계산 후 입력된 쪽만 덮어씀
        _apply_default_ylim(ax)
        lo0, hi0 = ax.get_ylim()
        ax.set_ylim(user_yl[0] if user_yl[0] is not None else lo0,
                    user_yl[1] if user_yl[1] is not None else hi0)
    else:
        # 기본: 모든 Read-out 그래프는 Y축 0 ~ (데이터 max의 110%)
        _apply_default_ylim(ax)
    # legend: 그래프(축) 바깥 위쪽에 가로 배치 → data와 절대 겹치지 않음
    ax.legend(fontsize=9 if screen else 6, ncol=max(1, len(model.readouts(lot))),
              loc="lower right", bbox_to_anchor=(1.0, 1.0),
              borderaxespad=0.0, frameon=True, handletextpad=0.4,
              columnspacing=0.8)
    ax._overlay_args = (model, lot, col, "line")   # layout 후 apply_overlays에서 렌더
    ax.grid(True, alpha=0.3)
    artists["__texts__"] = []
    return artists


def draw_delta(ax, model, lot, col, screen=False, picker=False):
    segs = None
    artists = {}
    for r in model.readouts(lot)[1:]:
        c = readout_color(model, lot, r)
        xs, ys, segs = model.seg_delta(lot, r, col)
        kw = dict(lw=0.9, color=c, label=r)
        if picker:
            kw["picker"] = 5
        line, = ax.plot(xs, ys, **kw)
        artists[r] = line
        in_x, in_y, out_x, out_y = [], [], [], []
        for x, v in zip(xs, ys):
            if math.isnan(v):
                continue
            if limit_out(model, lot, col, v, "delta"):
                out_x.append(x)
                out_y.append(v)
            else:
                in_x.append(x)
                in_y.append(v)
        ax.plot(in_x, in_y, marker="o", ms=3.5, ls="none", color=c)
        if out_x:
            ax.plot(out_x, out_y, marker="o", ms=4.5, mfc="none",
                    mec=_darken(c), mew=1.6, ls="none", zorder=4)
        for idx, (x, v) in enumerate(zip(xs, ys)):
            ph, s = model.pos_to_phase_sample(lot, col, idx)
            oc = model.color_over.get((lot, r, col, ph, s))
            if oc and not math.isnan(v):
                ax.plot([x], [v], marker="^", ms=6, color=oc,
                        markeredgecolor="black", markeredgewidth=0.6,
                        ls="none", zorder=5)
    if segs is None:
        xs, ys, segs = model.seg_delta(lot, model.readouts(lot)[0], col)
    ax.set_title(delta_title(model, lot, col), fontsize=9, loc="left")
    ax.set_ylabel("Delta (%)", fontsize=8)
    _seg_axis(ax, model, lot, col, segs, screen=screen)
    ax.axhline(0, color="gray", lw=0.8, ls="--", alpha=0.7)
    if len(model.readouts(lot)) > 1:
        ax.legend(fontsize=9 if screen else 6,
                  ncol=max(1, len(model.readouts(lot)) - 1),
                  loc="lower right", bbox_to_anchor=(1.0, 1.0),
                  borderaxespad=0.0, frameon=True, handletextpad=0.4,
                  columnspacing=0.8)
    else:
        ax.text(0.5, 0.5, "Read-out 1개 — Delta 없음", transform=ax.transAxes,
                ha="center", va="center", fontsize=9, color="gray")
    ax._overlay_args = (model, lot, col, "delta")
    ax.grid(True, alpha=0.3)
    artists["__texts__"] = []
    return artists


def draw_box(ax, model, lot, col, phase=None, stacked=False,
             picker=False, stats_table=True):
    """Box plot.
    phase 지정: 해당 phase만 / stacked=True: 모든 phase stack /
    공통 컬럼: phase=NO_PHASE로 호출."""
    readouts = model.readouts(lot)
    ph_arg = None if stacked else phase
    groups_vals = [model.box_values(lot, r, col, ph_arg) for r in readouts]
    try:
        bp = ax.boxplot(groups_vals, tick_labels=readouts, patch_artist=True,
                        showfliers=True, medianprops=dict(color="black"))
    except TypeError:
        bp = ax.boxplot(groups_vals, labels=readouts, patch_artist=True,
                        showfliers=True, medianprops=dict(color="black"))
    for patch, r in zip(bp["boxes"], readouts):
        patch.set_facecolor(readout_color(model, lot, r))
        patch.set_alpha(0.5)
    star_artists = []
    for i, (fl, r) in enumerate(zip(bp["fliers"], readouts)):
        c = readout_color(model, lot, r)
        yd = list(fl.get_ydata())
        in_l = [y for y in yd if not limit_out(model, lot, col, y)]
        out_l = [y for y in yd if limit_out(model, lot, col, y)]
        fl.set_xdata([i + 1] * len(in_l))
        fl.set_ydata(in_l)
        fl.set(marker="o", markerfacecolor=c,
               markeredgecolor="black", markersize=3)
        if picker:
            fl.set_picker(5)
        if out_l:  # limit 밖 outlier: 빈 동그라미 (진한 테두리, 굵고 크게)
            st, = ax.plot([i + 1] * len(out_l), out_l, marker="o", ms=4,
                          mfc="none", mec=_darken(c), mew=1.6,
                          ls="none", zorder=4)
            if picker:
                st.set_picker(5)
            st._star_readout = r
            star_artists.append(st)
    # 색 변경 삼각형 — 모든 Box 모드에서 표시 (stacked 포함)
    for (lt, r, c, ph, s), oc in model.color_over.items():
        if lt != lot or c != col:
            continue
        if not stacked and phase is not None and ph != phase:
            continue
        v = model.value(lot, r, c, ph, s)
        if v is not None and r in readouts:
            ax.plot([readouts.index(r) + 1], [v], marker="^", ms=5,
                    color=oc, ls="none", zorder=5)
    title_phase = None if stacked else phase
    ax.set_title(graph_title(model, lot, col, title_phase), fontsize=8, loc="left")
    ax.tick_params(labelsize=7)
    user_yl = model.ylim.get((lot, col))
    if user_yl and (user_yl[0] is not None or user_yl[1] is not None):
        lo0, hi0 = ax.get_ylim()
        ax.set_ylim(user_yl[0] if user_yl[0] is not None else lo0,
                    user_yl[1] if user_yl[1] is not None else hi0)
    ax._overlay_args = (model, lot, col, "box")
    text_artists = []
    ax.grid(True, alpha=0.3)

    if stats_table:
        trans = mtransforms.blended_transform_factory(ax.transData, ax.transAxes)
        ax.tick_params(axis="x", labelbottom=False)
        y0, dy, fs = -0.07, 0.075, 7   # 머리행은 축 바로 아래
        for i, r in enumerate(readouts):
            ax.text(i + 1, y0, r, transform=trans,
                    ha="center", va="center", fontsize=fs, fontweight="bold")
        rows = [("S/S", "SS"), ("Min", "Min"), ("Max", "Max"),
                ("AVG", "AVG"), ("STD", "STD")]
        for k, (label, key) in enumerate(rows):
            y = y0 - dy * (k + 2)   # 머리행과 통계 사이 한 줄 간격
            ax.text(-0.01, y, label, transform=ax.transAxes,
                    ha="right", va="center", fontsize=fs, fontweight="bold")
            for i, r in enumerate(readouts):
                st = model.stats(lot, r, col, ph_arg)
                v = st[key]
                txt = str(v) if key == "SS" else f"{v:.4g}"
                ax.text(i + 1, y, txt, transform=trans,
                        ha="center", va="center", fontsize=fs)
    else:
        ax.set_xlabel("Read-out", fontsize=8)
    bp["stars"] = star_artists
    bp["texts"] = text_artists
    return bp


